    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $comment = Comments::where('comment_id', $id);
        if($comment->count() > 0){
            if($comment->first()->reply_comment_id == null){
                $replyComment = Comments::where('comment_id', $comment->first()->reply_comment_id);
                if($replyComment->count() > 0){
                    $replyComment->delete();
                }
            }
            $comment->delete();
        }
    }
